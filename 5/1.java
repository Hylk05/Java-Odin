// Целочисленные типы
int intValue = 2;
switch(intValue) {
        case 1: break;
        case 2: break;
        }

byte byteValue = 1;
switch(byteValue) {
        case 1: break;
        }

char charValue = 'A';
switch(charValue) {
        case 'A': break;
        }

// String (с Java 7)
String stringValue = "test";
switch(stringValue) {
        case "test": break;
        }

// Enum
enum Color {RED, GREEN, BLUE}
Color color = Color.RED;
switch(color) {
        case RED: break;
        }